class Calculadora{
    suma(num1,num2){
        return num1+num2;
    }
    resta(num1,num2){
        return num1-num2;
    }
    multiplicacion(num1,num2){
        return num1*num2;
    }
    division(num1,num2){
        return num1/num2;
    }
    raiz(num1){
        return Math.sqrt(num1);
    }
    exp(num1,num2){
        return Math.pow(num1, num2);
    }
}

